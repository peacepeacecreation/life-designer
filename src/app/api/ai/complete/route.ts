import { NextRequest, NextResponse } from 'next/server'
import { getServerSession } from 'next-auth'
import { authOptions } from '../../auth/[...nextauth]/route'

/**
 * POST /api/ai/complete
 * AI completion endpoint using OpenRouter with Google Gemini
 */
export async function POST(request: NextRequest) {
  try {
    const session = await getServerSession(authOptions)
    if (!session?.user?.email) {
      return NextResponse.json({ error: 'Unauthorized' }, { status: 401 })
    }

    const { prompt, command, context } = await request.json()

    if (!prompt) {
      return NextResponse.json(
        { error: 'Prompt is required' },
        { status: 400 }
      )
    }

    const apiKey = process.env.OPENROUTER_API_KEY
    if (!apiKey) {
      return NextResponse.json(
        { error: 'OpenRouter API key not configured' },
        { status: 500 }
      )
    }

    // Different system prompts based on command
    const systemPrompts: Record<string, string> = {
      continue: 'Ти - помічник для написання тексту. Продовж текст природньо та логічно, зберігаючи стиль та тон. Відповідай ТІЛЬКИ продовженням тексту, без додаткових коментарів.',
      improve: 'Ти - редактор для покращення читабельності. Тобі надано виділений текст для покращення, а також контекст всієї нотатки для кращого розуміння теми. Перепиши ТІЛЬКИ виділений текст: прибери воду та зайві слова, зроби конструктивнішим і чіткішим, виділи саме важливе і унікальне. Враховуй контекст щоб зберегти термінологію та стиль. Використовуй markdown помірно: ## або ### для підзаголовків (якщо дійсно потрібно), - для списків. Уникай надмірного форматування, структура має бути природною. Відповідай ТІЛЬКИ покращеною версією виділеного тексту без додаткових коментарів.',
      summarize: 'Ти - помічник для створення підсумків. Створи короткий та змістовний підсумок наданого тексту. Відповідай ТІЛЬКИ підсумком.',
      expand: 'Ті - помічник для розширення ідей. Розшир надану думку, додай деталей, прикладів та роз\'яснень. Відповідай ТІЛЬКИ розширеною версією.',
      fixtable: 'Ти - експерт з форматування таблиць. Проаналізуй наданий текст та: 1) Якщо це таблиця (навіть у текстовому форматі з символами │├─┤┬┴└┘), перетвори її в чистий Markdown формат таблиці. 2) Виправ структуру, вирівняй колонки, покращ форматування. 3) Збережи весь контент та емодзі. Відповідай ТІЛЬКИ таблицею у Markdown форматі (| колонка | колонка |) без додаткових пояснень.',
      parsetable: 'Ти - парсер таблиць. Проаналізуй текст та розпізнай таблицю (навіть якщо вона у форматі з символами │├─┤┬┴└┘ або Markdown). Поверни ТІЛЬКИ JSON у форматі: {"rows": [["cell1", "cell2"], ["cell3", "cell4"]]} де кожен рядок - це масив комірок. Перший рядок - це заголовки. Збережи весь текст та емодзі. БЕЗ додаткових пояснень, ТІЛЬКИ JSON.',
      formatlist: 'Ти - форматувальник списків. Проаналізуй текст та перетвори його в структурований маркований список. Кожен пункт має бути на новому рядку, починатися з "-". Зроби структуру логічною та чіткою. Відповідай ТІЛЬКИ списком без додаткових пояснень.',
      formatnumberedlist: 'Ти - форматувальник нумерованих списків. Проаналізуй текст та перетвори його в структурований нумерований список. Кожен пункт має бути на новому рядку, починатися з "1.", "2.", "3." і т.д. Зроби структуру логічною та чіткою. Відповідай ТІЛЬКИ нумерованим списком без додаткових пояснень.',
      formatchecklist: 'Ти - створювач чек-листів. Проаналізуй текст та перетвори його в чек-лист з конкретними діями. Кожен пункт має бути на новому рядку, починатися з "- [ ]". Зроби пункти конкретними та виконуваними. Відповідай ТІЛЬКИ чек-листом без додаткових пояснень.',
      formatkeypoints: 'Ти - експерт з виділення ключових пунктів. Проаналізуй текст та виділи найважливіші ключові моменти у вигляді маркованого списку. Кожен пункт має бути на новому рядку, починатися з "-". Фокусуйся на найголовнішому, роби пункти короткими та змістовними. Відповідай ТІЛЬКИ списком ключових пунктів без додаткових пояснень.',
      formatactions: 'Ти - створювач списків дій. Проаналізуй текст та перетвори його в конкретні дії/кроки, які потрібно виконати. Кожна дія має бути на новому рядку, починатися з "- [ ]". Формулюй дії в активному дієсловному стилі (зробити, перевірити, створити і т.д.). Відповідай ТІЛЬКИ списком дій без додаткових пояснень.',
      fixstyle: 'Ти - експерт зі стилізації тексту. Тобі надано виділений текст та контекст всієї нотатки. Проаналізуй стиль нотатки (форматування, структуру, ієрархію заголовків, використання списків). Перепиши виділений текст щоб він відповідав стилю решти нотатки: зберігай ту саму ієрархію заголовків, формат списків, рівень деталізації. Якщо стиль виділеного фрагменту вже відповідає загальному стилю нотатки - залиш його майже незмінним, виправ лише очевидні невідповідності. Відповідай ТІЛЬКИ стилізованим текстом виділеного фрагменту без додаткових коментарів.',
      makeconcise: 'Ти - редактор для стислості. Зроби наданий текст коротшим, зберігаючи ключові ідеї та важливу інформацію. Видали зайве, залиши тільки суть. Відповідай ТІЛЬКИ скороченою версією тексту без додаткових пояснень.',
      removewater: 'Ти - експерт з покращення читабельності. Тобі надано виділений текст для покращення, а також контекст всієї нотатки. Перепиши ТІЛЬКИ виділений текст БЕЗ ВОДИ: прибери зайві слова, загальні фрази та пусті формулювання. Зроби текст конструктивним і чітким, виділи саме важливе і унікальне. Структуруй інформацію логічно. Враховуй контекст щоб зберегти термінологію та послідовність думок. Використовуй markdown помірно: ## або ### для підзаголовків (лише якщо це покращує читабельність), - для списків коли це доречно. Не переборщуй зі структурою - форматування має бути природним. Зберігай всі факти та ключові думки, але подавай їх максимально стисло та по суті. Відповідай ТІЛЬКИ переписаним текстом виділеного фрагменту без додаткових коментарів.',
      default: 'Ти - помічник для написання тексту. Допомагай користувачу писати, продовжувати та покращувати текст.'
    }

    const systemPrompt = systemPrompts[command || 'default'] || systemPrompts.default

    // Build user message with context if provided
    let userMessage = prompt
    if (context && context.trim()) {
      userMessage = `КОНТЕКСТ ВСІЄЇ НОТАТКИ:\n${context}\n\n---\n\nВИДІЛЕНИЙ ТЕКСТ ДЛЯ ОБРОБКИ:\n${prompt}`
    }

    // Call OpenRouter API with free Google Gemini model
    const response = await fetch('https://openrouter.ai/api/v1/chat/completions', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${apiKey}`,
        'Content-Type': 'application/json',
        'HTTP-Referer': process.env.NEXT_PUBLIC_APP_URL || 'http://localhost:3077',
        'X-Title': 'Life Designer'
      },
      body: JSON.stringify({
        model: 'openai/gpt-3.5-turbo', // Cheap and reliable: $0.50/$1.50 per 1M tokens
        messages: [
          {
            role: 'system',
            content: systemPrompt
          },
          {
            role: 'user',
            content: userMessage
          }
        ],
        temperature: 0.7,
        max_tokens: 500
      })
    })

    if (!response.ok) {
      const error = await response.text()
      console.error('OpenRouter API error:', error)
      return NextResponse.json(
        { error: 'AI service error' },
        { status: response.status }
      )
    }

    const data = await response.json()
    const completion = data.choices?.[0]?.message?.content

    if (!completion) {
      return NextResponse.json(
        { error: 'No completion generated' },
        { status: 500 }
      )
    }

    return NextResponse.json({ completion })
  } catch (error) {
    console.error('Error in AI completion:', error)
    return NextResponse.json(
      { error: 'Internal server error' },
      { status: 500 }
    )
  }
}
